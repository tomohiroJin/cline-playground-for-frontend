/**
 * 敵タイプからスプライトシートを取得するヘルパー
 *
 * @param enemyType - 敵タイプの文字列識別子
 * @returns 対応するスプライトシート定義
 * @throws 未知の敵タイプが指定された場合エラーをスロー
 */
export function getEnemySpriteSheet(enemyType: string): SpriteSheetDefinition {
  switch (enemyType) {
    case 'patrol':
      return PATROL_SPRITE_SHEET;
    case 'charge':
      return CHARGE_SPRITE_SHEET;
    case 'ranged':
      return RANGED_SPRITE_SHEET;
    case 'specimen':
      return SPECIMEN_SPRITE_SHEET;
    case 'boss':
      return BOSS_SPRITE_SHEET;
    default:
      throw new Error(`不明な敵タイプ: ${enemyType}`);
  }
}
