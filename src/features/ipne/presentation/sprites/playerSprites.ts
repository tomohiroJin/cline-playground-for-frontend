/**
 * ピクセルデータとパレットから SpriteDefinition を生成する
 *
 * @param pixels - 16×16 のパレットインデックス配列
 * @param palette - カラーパレット
 * @returns SpriteDefinition
 */
function createSpriteDefinition(
  pixels: number[][],
  palette: string[]
): SpriteDefinition {
  return {
    width: 16,
    height: 16,
    pixels,
    palette,
  };
}

/**
 * スプライトシート定義を生成する
 *
 * @param frames - 3フレーム分の SpriteDefinition 配列 [idle, walk1, walk2]
 * @returns SpriteSheetDefinition
 */
function createSheet(frames: SpriteDefinition[]): SpriteSheetDefinition {
  return {
    sprites: frames,
    frameDuration: 200,
  };
}

