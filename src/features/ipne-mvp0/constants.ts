import { GameMap } from './types';

export const MAP_WIDTH = 20;
export const MAP_HEIGHT = 20;

// MVP0 Fixed Map (20x20)
// S: Start, G: Goal, .: Floor, #: Wall
const RAW_MAP = [
  '####################',
  '#S.................#',
  '#.###.##.#########.#',
  '#.#......#.......#.#',
  '#.#.####.#.#####.#.#',
  '#......#.#.#...#.#.#',
  '####.#.#.#.#.#.#.#.#',
  '#....#...#...#...#.#',
  '#.####.###.###.###.#',
  '#.#......#.#.....#.#',
  '#.#.####.#.#####.#.#',
  '#.#.#....#.......#.#',
  '#.#.#.##.#######.#.#',
  '#.#...#..........#.#',
  '#.#####.########.#.#',
  '#.......#......#...#',
  '#######.#.####.###.#',
  '#.......#.#....#...#',
  '#.#######.#.####.G.#',
  '####################',
];

export const FIXED_MAP: GameMap = RAW_MAP.map(row =>
  row.split('').map(char => {
    switch (char) {
      case '#':
        return 'Wall';
      case 'S':
        return 'Start';
      case 'G':
        return 'Goal';
      default:
        return 'Floor';
    }
  })
);
